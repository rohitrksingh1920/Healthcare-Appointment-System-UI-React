function generateSlots() {
  const morningSlots = ["09:00 AM", "09:30 AM", "10:00 AM", "10:30 AM", "11:00 AM", "11:30 AM"];
  const afternoonSlots = ["01:00 PM", "01:30 PM", "02:00 PM", "02:30 PM", "03:00 PM", "03:30 PM"];
  const eveningSlots = ["05:00 PM", "05:30 PM", "06:00 PM", "06:30 PM"];
  
  const bookedSlots = [];
  [...morningSlots, ...afternoonSlots, ...eveningSlots].forEach(slot => {
    if (Math.random() > 0.6) bookedSlots.push(slot);
  });
  
  return {
    morning: morningSlots,
    afternoon: afternoonSlots,
    evening: eveningSlots,
    booked: bookedSlots,
  };
}

